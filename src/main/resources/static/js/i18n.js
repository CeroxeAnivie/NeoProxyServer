/**
 * 切换语言 — 更新所有 data-i18n 元素的内容
 * @param {string} l 语言代码: 'zh' | 'en'
 */
function setLang(l) {
    curLang = l;
    for (let k in LANG[l]) {
        const els = document.querySelectorAll(`[data-i18n="${k}"]`);
        els.forEach(el => el.innerHTML = LANG[l][k]);
        const elsP = document.querySelectorAll(`[data-i18n-placeholder="${k}"]`);
        elsP.forEach(el => el.placeholder = LANG[l][k]);
        const elsT = document.querySelectorAll(`[data-i18n-title="${k}"]`);
        elsT.forEach(el => el.title = LANG[l][k]);
    }

    document.querySelectorAll('.ctrl-btn').forEach(b => b.classList.remove('active'));
    const langBtn = document.querySelector(`.ctrl-btn[onclick="setLang('${l}')"]`);
    if (langBtn) langBtn.classList.add('active');

    if (currentTab === 'keys' && lastKeyData) renderKeyTable(lastKeyData);
    else if (currentTab === 'clients' && lastClientData) renderClientTable(lastClientData);
    else if (currentTab === 'bans' && lastBanData) renderBanTable(lastBanData);

    updateHeader();
}


